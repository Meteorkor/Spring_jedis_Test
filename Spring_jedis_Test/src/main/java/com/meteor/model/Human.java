package com.meteor.model;

public class Human {

	String name;
	String age;
	public String getName() {
		return name;
	}
	public Human setName(String name) {
		this.name = name;
		return this;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	
	
	
}
